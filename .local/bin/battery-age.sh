#read -p "Enter your birth year.  Example: 1900 : " year
#read -p "Enter your birht month. Example: 06 " month
year=2019
month=05

yearnow=$(date '+%Y')
monthnow=$(date '+%m')

agey=$(($yearnow-$year))
agem=$((10#$monthnow-$month))

if [[ $((10#$agem)) -lt 0 ]] ; then
   agem=$(($monthnow-$month+12))
else
   agem=$((10#$monthnow-$month))
fi

echo "$agey""Y" "$agem""M"

